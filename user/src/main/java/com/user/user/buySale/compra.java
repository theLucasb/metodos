public class compra<numeric> {
    private numeric ask_min;
    private numeric ask_max;
    private numeric bid_min;
    private numeric bid_max;

    public numeric getAsk_min() {
        return ask_min;
    }

    public void setAsk_min(numeric ask_min) {
        this.ask_min = ask_min;
    }

    public numeric getAsk_max() {
        return ask_max;
    }

    public void setAsk_max(numeric ask_max) {
        this.ask_max = ask_max;
    }

    public numeric getBid_min() {
        return bid_min;
    }

    public void setBid_min(numeric bid_min) {
        this.bid_min = bid_min;
    }

    public numeric getBid_max() {
        return bid_max;
    }

    public void setBid_max(numeric bid_max) {
        this.bid_max = bid_max;
    }

}
